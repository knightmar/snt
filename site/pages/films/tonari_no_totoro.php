        voisins, invisible aux yeux des autres humains, des créatures merveilleuses, mais très discrètes : Grand Totoro,
        Moyen Totoro (Chū-Totoro) et Petit Totoro (Chibi-Totoro). Avec son ventre rebondi, Totoro est un être rare et
        fascinant, un esprit de la forêt... Il se nourrit de glands et de noix. Il peut voler, se déplacer en « Chat-Bus
        ». Il dort le jour, mais les nuits de pleine lune, il aime jouer avec des ocarinas magiques...</p>
