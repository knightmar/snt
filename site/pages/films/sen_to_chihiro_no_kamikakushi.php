    <h3>Histoire :</h3>
    <p>Le Voyage de Chihiro (千と千尋の神隠し, Sen to Chihiro no kamikakushi)
        est un film d'animation japonais écrit et réalisé par Hayao Miyazaki et produit par le studio Ghibli, sorti en
        2001.
    </p>
    <br>
    <p>Le Voyage de Chihiro fut jusqu'en 2020 le plus grand succès de l'histoire du cinéma japonais, avec 23 millions de
        spectateurs au Japon et 274 millions de dollars de recettes dans le monde. Acclamé par la critique
        internationale, le film est considéré comme l'un des meilleurs des années 2000. Il remporte plusieurs
        récompenses, dont l'Oscar du meilleur film d'animation et l'Ours d'or du meilleur film de 2002.</p>


    <h3>Résumé :</h3>
    <p>Chihiro est une petite fille de dix ans, grincheuse et gâtée, recroquevillée à l’arrière de la voiture de ses
        parents. Ils approchent de leur nouvelle maison, et elle est triste de quitter sa vie d’avant. Par erreur,
        s’étant engagés dans une « forêt obscure », ils se retrouvent dans un parc de loisirs abandonné. Par goinfrerie,
        ayant perdu la « voie droite », le père et la mère sont magiquement transformés en cochons. Chihiro est alors
        brutalement embarquée dans un cauchemar incompréhensible au royaume des ombres. Elle devient l’humble
        travailleuse, d’abord clandestine puis sous contrat – mais la patronne est une sorcière diabolique – d’un
        immense établissement de bains anachronique, qui oeuvre au repos temporaire des innombrables esprits de la
        nature de la tradition shintoïste.</p>
